// Neighborhood data for SEO-focused neighborhood pages

export interface Neighborhood {
  slug: string;
  name: string;
  city: string;
  cityName: string;
  state: string;
  description: string;
  homeStyles: string;
  established: string;
  characteristics: string;
  roofingChallenges: string;
  highlights: string[];
  nearbyNeighborhoods: string[];
}

// Utility function to convert neighborhood name to URL slug
export function slugifyNeighborhood(name: string): string {
  return name
    .toLowerCase()
    .replace(/['']/g, '')
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/^-+|-+$/g, '');
}

// Get neighborhood by city and slug
export function getNeighborhood(citySlug: string, neighborhoodSlug: string): Neighborhood | undefined {
  const cityNeighborhoods = NEIGHBORHOODS[citySlug];
  if (!cityNeighborhoods) return undefined;
  return cityNeighborhoods.find(n => n.slug === neighborhoodSlug);
}

// Get all neighborhoods for a city
export function getNeighborhoodsByCity(citySlug: string): Neighborhood[] {
  return NEIGHBORHOODS[citySlug] || [];
}

// Get all neighborhood params for static generation
export function getAllNeighborhoodParams(): { city: string; neighborhood: string }[] {
  const params: { city: string; neighborhood: string }[] = [];

  for (const [citySlug, neighborhoods] of Object.entries(NEIGHBORHOODS)) {
    for (const neighborhood of neighborhoods) {
      params.push({
        city: citySlug,
        neighborhood: neighborhood.slug,
      });
    }
  }

  return params;
}

