// Neighborhood data for SEO-focused neighborhood pages

export interface HousingStats {
  medianAge: string;          // "1960s" or "Built 1985-1995"
  commonRoofTypes: string[];  // ['Asphalt Shingle', 'Architectural']
  avgSquareFootage: string;   // "2,200-3,500 sq ft"
  priceRange: string;         // "$350K-$650K"
}

export interface LocalData {
  landmarks: string[];        // Nearby landmarks
  schools: string[];          // School districts/names
  hoaInfo: string;            // HOA requirements for roofing
  stormHistory: string;       // Notable storm events
  treeCanopy: string;         // "Heavy", "Moderate", "Light"
}

export interface ProofOfWork {
  projectCount?: number;      // Jobs completed in area
  avgRating?: number;         // Average review score
  recentProject?: string;     // Description of recent job
}

export interface Neighborhood {
  slug: string;
  name: string;
  city: string;
  cityName: string;
  state: string;
  description: string;
  homeStyles: string;
  established: string;
  characteristics: string;
  roofingChallenges: string;
  highlights: string[];
  nearbyNeighborhoods: string[];

  // NEW: Zip Code & Geographic Data
  zipCodes: string[];         // e.g., ['28207', '28209']
  primaryZip: string;         // Main zip for the neighborhood

  // NEW: Housing Stock Details
  housingStats: HousingStats;

  // NEW: Local Context
  localData: LocalData;

  // NEW: Proof of Work (optional - populated from ProjectMapIt)
  proofOfWork?: ProofOfWork;
}

// Utility function to convert neighborhood name to URL slug
export function slugifyNeighborhood(name: string): string {
  return name
    .toLowerCase()
    .replace(/['']/g, '')
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/^-+|-+$/g, '');
}

// Get neighborhood by city and slug
export function getNeighborhood(citySlug: string, neighborhoodSlug: string): Neighborhood | undefined {
  const cityNeighborhoods = NEIGHBORHOODS[citySlug];
  if (!cityNeighborhoods) return undefined;
  return cityNeighborhoods.find(n => n.slug === neighborhoodSlug);
}

// Get all neighborhoods for a city
export function getNeighborhoodsByCity(citySlug: string): Neighborhood[] {
  return NEIGHBORHOODS[citySlug] || [];
}

// Get all neighborhood params for static generation
export function getAllNeighborhoodParams(): { city: string; neighborhood: string }[] {
  const params: { city: string; neighborhood: string }[] = [];

  for (const [citySlug, neighborhoods] of Object.entries(NEIGHBORHOODS)) {
    for (const neighborhood of neighborhoods) {
      params.push({
        city: citySlug,
        neighborhood: neighborhood.slug,
      });
    }
  }

  return params;
}

